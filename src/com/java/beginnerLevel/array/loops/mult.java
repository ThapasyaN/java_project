package com.java.beginnerLevel.array.loops;

public class mult
{
    public static void main(String[] args) {
        int x;
        for (x = 1; x <= 5; x++)
        {
            System.out.println(x + " .thapasya");

            System.out.println("The Value Of x: " + x);
            System.out.println();

            int y = 1;
            while (y <= 5) {
                System.out.println(y + ".sandeep");
                y++;
            }
            System.out.println("the value of y:" + y);
            System.out.println();

            int z = 1;
            do {
                System.out.println(z + ".HAPPY BIRTHDAY ^-^");
                z++;
            }
            while (z <= 5);
            System.out.println("the value of z:" + z);
            System.out.println();

        }
     }
    }




