package com.java.beginnerLevel.array.loops;

public class WhileApp
{
    public static void main(String[] args)
    {
        int t=1;
        while(t <= 4)
        {
            System.out.println(t + ".HAPPY BIRTHDAY TO YOU ^-^  *-* ");
            t++;
        }
        System.out.println("the value of t: " +t);

    }
}
